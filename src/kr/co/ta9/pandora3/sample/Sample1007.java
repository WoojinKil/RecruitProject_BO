package kr.co.ta9.pandora3.sample;

import kr.co.ta9.pandora3.app.bean.CommonBean;

//샘플 팀별 실적 현황 VO
public class Sample1007 extends CommonBean {
	
	
	private int id;                
	private String invdate;
	private String name;
	private String note;
	private int amonut;
	private int tax;
	private int total;
	
	public long getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getInvdate() {
		return invdate;
	}
	public void setInvdate(String invdate) {
		this.invdate = invdate;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public int getAmonut() {
		return amonut;
	}
	public void setAmonut(int amonut) {
		this.amonut = amonut;
	}
	public int getTax() {
		return tax;
	}
	public void setTax(int tax) {
		this.tax = tax;
	}
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	
	
	
}
