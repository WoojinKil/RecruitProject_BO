$(function() {
	swiperFunc();
});

function swiperFunc() {
	/* main-swiper1 */
	new Swiper('.main-swiper1', {
		loop: true,
		pagination: {
			el: '.swiper-pagination1',
			clickable: true,
		},
		autoplay: {
			delay: 2000,
			disableOnInteraction: false,
		},
	});

	new Swiper('.main-swiper2', {
		loop: true,
		slidesPerView: "auto",
		spaceBetween: 15,
		autoplay: {
			delay: 2000,
			disableOnInteraction: false
		},
	});
	
}